/**
 * @nan0web/icons — Build-time generator
 *
 * Reads react-icons source, extracts SVG data from GenIcon() calls,
 * and writes framework-agnostic icon modules to src/sets/*.js
 *
 * Usage: node scripts/generate.js [set1] [set2] ...
 * Example: node scripts/generate.js bs fa fi
 * (no args = generate all configured sets)
 */

