#!/usr/bin/env node
import process from 'node:process'
import { readdir, readFile, stat } from 'node:fs/promises'
import path from 'node:path'
import FS from '@nan0web/db-fs'
import Logger from '@nan0web/log'
import { createOutputProgress, pause } from '../src/cli.js'
import { execSync } from 'node:child_process'

const logger = new Logger(Logger.detectLevel(process.argv))

/** Extract metadata from markdown files */
async function parseMarkdownStatus(filePath) {
	try {
		const content = await readFile(filePath, 'utf-8')
		const lines = content.split('\n')
		let title = ''
		let tasks = 0
		let completed = 0
		let isYaml = false
		for (const line of lines) {
			const tline = line.trim()
			
			if (tline.includes('[ ]')) tasks++
			if (tline.includes('[x]') || tline.includes('[X]')) { tasks++; completed++; }

			if (tline === '---') {
				isYaml = !isYaml
				continue
			}
			if (isYaml && tline.startsWith('description:')) {
				title = tline.slice(12).trim()
				continue
			}
			
			if (!title) {
				if (tline.startsWith('# ')) title = tline.slice(2).trim()
				else if (tline.startsWith('## ')) title = tline.slice(3).trim()
			}
		}
		
		return {
			exists: true,
			title: title || path.basename(filePath),
			tasks,
			completed
		}
	} catch (e) {
		return { exists: false }
	}
}

async function scanDirectory(subDir) {
	const rootPath = path.resolve(process.cwd(), subDir)
	const items = []
	try {
		const names = await readdir(rootPath)
		for (const name of names) {
			const pkgPath = path.join(rootPath, name)
			const st = await stat(pkgPath).catch(() => null)
			if (!st?.isDirectory() || name.startsWith('.') || name === 'node_modules') continue
			
			const results = {}
			const targets = ['project.md', 'seed.md', 'next.md', 'REQUESTS.md', 'docs/next.md', 'docs/uk/next.md', 'docs/en/next.md']
			
			// If project.md exists, validate it and generate project.yaml
			const pmdPath = path.join(pkgPath, 'project.md')
			const pmdStat = await stat(pmdPath).catch(() => null)
			if (pmdStat) {
				try {
					execSync(`node bin/project-validator.js "${pmdPath}"`, { stdio: 'ignore' })
				} catch (e) {
					// Silent fail for validation during audit
				}
			}

			for (const t of targets) {
				const res = await parseMarkdownStatus(path.join(pkgPath, t))
				if (res.exists) {
					results[t] = res
				}
			}
			if (Object.keys(results).length) {
				items.push({ name, type: subDir, path: pkgPath, files: results })
			}
		}
	} catch (e) {
		logger.warn(`Failed to read ${rootPath}: ${e.message}`)
	}
	return items
}

async function main(argv = process.argv.slice(2)) {
	logger.info(Logger.style(Logger.LOGO, { color: Logger.MAGENTA }))
	
	const db = new FS({ root: 'dist/audit' })
	await db.connect()
	
	const rootDb = new FS({ root: '.' })
	await rootDb.connect()

	let chunks = ['Auditing ecosystem architecture...']
	
	const start = async (options = {}, fn) => {
		const opts = {
			...options,
			logger: process.stdout.isTTY ? logger : null,
			fps: 33,
		}
		const interval = createOutputProgress(opts)
		const result = await fn()
		clearInterval(interval)
		logger.cursorUp(opts.printed || 0, true)
		return result
	}

	let scanned = []
	await start({ chunks }, async () => {
		const pkgs = await scanDirectory('packages')
		const apps = await scanDirectory('apps')
		scanned = [...pkgs, ...apps]
		await pause(500)
	})

	logger.info(logger.fill(`Audit completed. Found ${scanned.length} active modules.`))

	await db.saveDocument('status.json', scanned)
	
	let mdContent = '# 🏗️ Ecosystem Architectural Status\n\n'
	mdContent += `> Generated by bin/audit.js on ${new Date().toISOString()}\n\n`
	
	const grouped = scanned.reduce((acc, item) => {
		if (!acc[item.type]) acc[item.type] = []
		acc[item.type].push(item)
		return acc
	}, {})

	for (const type of Object.keys(grouped).sort()) {
		mdContent += `## 📁 ${type.toUpperCase()}\n\n`
		mdContent += `| Module | Project Goal | Status | Tasks | Docs |\n`
		mdContent += `|--------|--------------|--------|-------|------|\n`
		for (const item of grouped[type].sort((a,b) => a.name.localeCompare(b.name))) {
			const pMd = item.files['project.md'] || item.files['seed.md']
			let goal = pMd && pMd.title ? pMd.title : '*Planning*'
			goal = goal.replace(/\|/g, '\\|')
			
			const activeDocs = Object.keys(item.files).filter(f => f !== 'project.md')
			
			let tTasks = 0
			let tComp = 0
			for (const fp of Object.keys(item.files)) {
				tTasks += item.files[fp].tasks || 0
				tComp += item.files[fp].completed || 0
			}
			
			let statusStr = '⚪️ Idle'
			let progressStr = '-'
			if (tTasks > 0) {
				const pct = Math.round((tComp / tTasks) * 100)
				progressStr = `${tComp}/${tTasks}`
				if (pct === 100) statusStr = `✅ Done`
				else if (pct >= 50) statusStr = `🟡 Active (${pct}%)`
				else statusStr = `🔴 Early (${pct}%)`
			} else if (activeDocs.length > 0) {
				statusStr = '🔵 Planning'
			}
			
			const docList = Object.keys(item.files).map(f => `[${f.split('/').pop()}](./${item.type}/${item.name}/${f})`).join(', ')

			mdContent += `| **${item.name}** | ${goal} | ${statusStr} | ${progressStr} | ${docList} |\n`
		}
		mdContent += `\n`
	}
	
	await rootDb.saveDocument('STATUS.md', mdContent)
	logger.success(`Ecosystem report updated: STATUS.md`)
}

main().catch(e => {
	const errLogger = new Logger(Logger.detectLevel(process.argv))
	errLogger.error('Audit failed')
	errLogger.error(e.message)
	process.exit(1)
})
